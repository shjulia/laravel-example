<p><strong>A Summary of Your Rights Under the Fair Credit Reporting Act</strong></p>

<p>The federal Fair Credit Reporting Act (FCRA) promotes the accuracy,
fairness, and privacy of information in the files of consumer reporting
agencies. There are many types of consumer reporting agencies, including
credit bureaus and specialty agencies (such as agencies that sell
information about check writing histories, medical records, and rental
history records). Here is a summary of your major rights under FCRA.
<strong>For more information, including information about additional rights,
go to
<a href="http://www.consumerfinance.gov/learnmore">www.consumerfinance.gov/learnmore</a>
or write to: Consumer Financial Protection Bureau, 1700 G Street N.W.,
Washington, DC 20552.</strong></p>

<ul>
  <li>
    <p><strong>You must be told if information in your file has been used against
you.</strong> Anyone who uses a credit report or another type of consumer
report to deny your application for credit, insurance, or employment
– or to take another adverse action against you – must tell you,
and must give you the name, address, and phone number of the agency
that provided the information.</p>
  </li>
  <li>
    <p><strong>You have the right to know what is in your file.</strong> You may request
and obtain all the information about you in the files of a consumer
reporting agency (your “file disclosure”). You will be required to
provide proper identification, which may include your Social
Security number. In many cases, the disclosure will be free. You are
entitled to a free file disclosure if:</p>

    <ul>
      <li>
        <p>a person has taken adverse action against you because of
information in your credit report;</p>
      </li>
      <li>
        <p>you are the victim of identity theft and place a fraud alert in
your file;</p>
      </li>
      <li>
        <p>your file contains inaccurate information as a result of fraud;</p>
      </li>
      <li>
        <p>you are on public assistance;</p>
      </li>
      <li>
        <p>you are unemployed but expect to apply for employment within 60
days.</p>

        <p>In addition, all consumers are entitled to one free disclosure
every 12 months upon request from each nationwide credit bureau
and from nationwide specialty consumer reporting agencies. See
<a href="http://www.consumerfinance.gov/learnmore">www.consumerfinance.gov/learnmore</a>
for additional information.</p>
      </li>
    </ul>
  </li>
  <li>
    <p><strong>You have the right to ask for a credit score.</strong> Credit scores are
numerical summaries of your credit-worthiness based on information
from credit bureaus. You may request a credit score from consumer
reporting agencies that create scores or distribute scores used in
residential real property loans, but you will have to pay for it. In
some mortgage transactions, you will receive credit score
information for free from the mortgage lender.</p>
  </li>
  <li>
    <p><strong>You have the right to dispute incomplete or inaccurate
information.</strong> If you identify information in your file that is
incomplete or inaccurate, and report it to the consumer reporting
agency, the agency must investigate unless your dispute is
frivolous. See
<a href="http://www.consumerfinance.gov/learnmore">www.consumerfinance.gov/learnmore</a>
for an explanation of dispute procedures.</p>
  </li>
  <li>
    <p><strong>Consumer reporting agencies must correct or delete inaccurate,
incomplete, or unverifiable information.</strong> Inaccurate, incomplete,
or unverifiable information must be removed or corrected, usually
within 30 days. However, a consumer reporting agency may continue to
report information it has verified as accurate.</p>
  </li>
  <li>
    <p><strong>Consumer reporting agencies may not report outdated negative
information.</strong> In most cases, a consumer reporting agency may not
report negative information that is more than seven years old, or
bankruptcies that are more than 10 years old.</p>
  </li>
  <li>
    <p><strong>Access to your file is limited.</strong> A consumer reporting agency may
provide information about you only to people with a valid need –
usually to consider an application with a creditor, insurer,
employer, landlord, or other business. The FCRA specifies those with
a valid need for access.</p>
  </li>
  <li>
    <p><strong>You must give your consent for reports to be provided to
employers.</strong> A consumer reporting agency may not give out
information about you to your employer, or a potential employer,
without your written consent given to the employer. Written consent
generally is not required in the trucking industry. For more
information, go to
<a href="http://www.consumerfinance.gov/learnmore">www.consumerfinance.gov/learnmore</a>.</p>
  </li>
  <li>
    <p><strong>You may limit “prescreened” offers of credit and insurance you get
based on information in your credit report.</strong> Unsolicited
“prescreened” offers for credit and insurance must include a
toll-free phone number you can call if you choose to remove your
name and address form the lists these offers are based on. You may
opt out with the nationwide credit bureaus at 1-888-5-OPTOUT
(1-888-567-8688).</p>
  </li>
  <li>
    <p>The following FCRA right applies with respect to nationwide consumer
reporting agencies<strong>:</strong></p>

    <h2 class="text-center mt-5" style="font-variant: small-caps; text-rendering: auto;">Consumers Have The Right To Obtain A Security Freeze</h2>

    <p><strong>You have a right to place a “security freeze” on your credit
report, which will prohibit a consumer reporting agency from
releasing information in your credit report without your express
authorization.</strong> The security freeze is designed to prevent credit,
loans, and services from being approved in your name without your
consent. However, you should be aware that using a security freeze
to take control over who gets access to the personal and financial
information in your credit report may delay, interfere with, or
prohibit the timely approval of any subsequent request or
application you make regarding a new loan, credit, mortgage, or any
other account involving the extension of credit.</p>

    <p>As an alternative to a security freeze, you have the right to place
an initial or extended fraud alert on your credit file at no cost.
An initial fraud alert is a 1-year alert that is placed on a
consumer’s credit file. Upon seeing a fraud alert display on a
consumer’s credit file, a business is required to take steps to
verify the consumer’s identity before extending new credit. If you
are a victim of identity theft, you are entitled to an extended
fraud alert, which is a fraud alert lasting 7 years.</p>

    <p>A security freeze does not apply to a person or entity, or its
affiliates, or collection agencies acting on behalf of the person or
entity, with which you have an existing account that requests
information in your credit report for the purposes of reviewing or
collecting the account. Reviewing the account includes activities
related to account maintenance, monitoring, credit line increases,
and account upgrades and enhancements.</p>
  </li>
  <li>
    <p><strong>You may seek damages from violators.</strong> If a consumer reporting
agency, or, in some cases, a user of consumer reports or a furnisher
of information to a consumer reporting agency violates the FCRA, you
may be able to sue in state or federal court.</p>
  </li>
  <li>
    <p><strong>Identity theft victims and active duty military personnel have
additional rights.</strong> For more information, visit
<a href="http://www.consumerfinance.gov/learnmore">www.consumerfinance.gov/learnmore</a>.</p>
  </li>
</ul>

